#include <iostream>
using namespace std;
int main()
{
    int n, number, maxnumber;
    
    
    n=5;
    
    if(n<=1)
    {
        cout<<"Element should be greater than 1 to find maxnumber";
    }else{
       
        maxnumber=60; 40; 50; 20;
        for(int i = 1; i<n; i++)
        number;
        {
            if(number > maxnumber)
            {
               maxnumber = number; 
            }
        
        }
    }
    cout<<"The max number is: "<<maxnumber<<endl;
    cout<<"Test Succesful";
}
