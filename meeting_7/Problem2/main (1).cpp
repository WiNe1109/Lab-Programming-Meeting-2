#include <iostream>
using namespace std;

int fact(int x){
    if (x == 0 || x == 1){
    return 1;
    }
    else
    return x * fact(x-1);
}
int main(){
    int x;
    cout <<"Enter X Factorial: ";
    cin >>x;
    
    if(x<0){
        cout <<"Negative Number is invalid in Factorial";
    }
    else{
        cout <<"Factorial of " << x <<" is " << fact(x);
        }
}
